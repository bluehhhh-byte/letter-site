var TAROT_DECK = [];
const majorSymbols = ['🃏','✨','🌙','🌸','👑','📿','💞','🏛️','🦁','🏮','☯️','⚖️','🪢','💀','🌊','😈','🗼','⭐','🌙','☀️','📯','🌍'];
const minorSymbols = { wands:'🗡', cups:'🏺', swords:'⚔️', pentacles:'🪙' };

TAROT_MAJOR.forEach(([id, name, nameEn, keywords, upright, reversed]) => {
  TAROT_DECK.push({
    type:'major', suit:null, rank:id, name, nameEn, keywords,
    upright, reversed, symbol: majorSymbols[id] || '🔮'
  });
});

const suitKeys = ['wands','cups','swords','pentacles'];
MINOR_MEANINGS.forEach(([suitIdx, rank, upright, reversed]) => {
  const suit = TAROT_SUITS[suitKeys[suitIdx]];
  TAROT_DECK.push({
    type:'minor', suit:suitKeys[suitIdx], suitName:suit.name, element:suit.element,
    rank, keywords:suit.desc,
    name: (rank === 1 ? '에이스' : (rank <= 10 ? rank + '' : MINOR_NAMES[rank])) + ' ' + suit.name,
    nameEn: (['Ace','Two','Three','Four','Five','Six','Seven','Eight','Nine','Ten','Page','Knight','Queen','King'][rank-1]) + ' of ' + suit.nameEn,
    upright, reversed, symbol: minorSymbols[suitKeys[suitIdx]]
  });
});
